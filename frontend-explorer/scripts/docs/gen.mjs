// Build-time generator: turns the repo-root `docs/*.md` set into a committed
// TypeScript module the app imports (`lib/docs/generated.ts`). Run via the
// `predev` / `prebuild` / `pretest` npm hooks, and committed so typecheck/lint
// pass without a generate step. Zero runtime deps — see `render.mjs`.

import { readFileSync, writeFileSync, readdirSync, mkdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { parseFrontMatter, mdToHtml, buildSearchRecords } from "./render.mjs";

const HERE = dirname(fileURLToPath(import.meta.url));
const DOCS_DIR = join(HERE, "..", "..", "..", "docs");
const OUT = join(HERE, "..", "..", "lib", "docs", "generated.ts");
const PARTS = ["Understand", "Use", "Build & Operate", "Reference"];

function walk(dir, base = "") {
  const files = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const rel = base ? `${base}/${entry.name}` : entry.name;
    if (entry.isDirectory()) files.push(...walk(join(dir, entry.name), rel));
    else if (entry.name.endsWith(".md")) files.push(rel);
  }
  return files;
}

const pages = [];
for (const rel of walk(DOCS_DIR)) {
  const raw = readFileSync(join(DOCS_DIR, rel), "utf8");
  const { data, body } = parseFrontMatter(raw);
  const slug = rel.replace(/\.md$/, "") === "index" ? "" : rel.replace(/\.md$/, "");
  const route = slug ? `/docs/${slug}` : "/docs";
  const { html, headings } = mdToHtml(body);
  pages.push({
    slug,
    route,
    title: data.title || slug || "Docs",
    part: data.part || "",
    order: data.order ? Number(data.order) : 999,
    summary: data.summary || "",
    keywords: Array.isArray(data.keywords) ? data.keywords : [],
    html,
    headings,
  });
}

pages.sort((a, b) => a.order - b.order);

const search = [];
for (const p of pages) search.push(...buildSearchRecords(p));

const banner = `// AUTO-GENERATED by scripts/docs/gen.mjs from the repo-root docs/*.md set.
// Do not edit by hand — run \`pnpm gen:docs\` (also runs on predev/prebuild/pretest).
/* eslint-disable */
`;

const types = `
export interface DocHeading { depth: number; text: string; slug: string; }
export interface DocPage {
  slug: string; route: string; title: string; part: string;
  order: number; summary: string; keywords: string[];
  html: string; headings: DocHeading[];
}
export interface DocSearchRecord {
  route: string; anchor: string; pageTitle: string; part: string;
  heading: string; text: string; keywords: string[];
}
`;

const content =
  banner +
  types +
  `\nexport const DOCS_PARTS: string[] = ${JSON.stringify(PARTS)};\n` +
  `\nexport const DOCS_PAGES: DocPage[] = ${JSON.stringify(pages, null, 2)};\n` +
  `\nexport const DOCS_SEARCH: DocSearchRecord[] = ${JSON.stringify(search, null, 2)};\n`;

mkdirSync(dirname(OUT), { recursive: true });
writeFileSync(OUT, content);
console.log(`[gen:docs] wrote ${pages.length} pages, ${search.length} search records → lib/docs/generated.ts`);
